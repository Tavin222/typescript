"use strict";
// Função de multiplicação
function multiplicacao(num1, num2) {
    return num1 * num2;
}
// Função de saudação
function saudacao(nome) {
    return `Olá ${nome}`;
}
// Exemplos de uso das funções
console.log(multiplicacao(5, 3)); // Saída: 15
console.log(saudacao("João")); // Saída: Olá João
